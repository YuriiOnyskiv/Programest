        <h2 class="text-center">Web design</h2>
        <p><i>Web design</i> refers to the design of websites that are displayed on the internet. It usually refers to the user experience aspects of website development rather than software development. Web design used to be focused on designing websites for desktop browsers; however, since the mid-2010s, design for mobile and tablet browsers has become ever-increasingly important.
          <br><br>
          <u>A web designer</u> works on the appearance, layout, and, in some cases, content of a website. Appearance, for instance, relates to the colors, font, and images used. Layout refers to how information is structured and categorized. A good web design is easy to use, aesthetically pleasing, and suits the user group and brand of the website.
        </p>
        <p>Two of the most common methods for designing websites that work well both on desktop and mobile are <strong>responsive</strong> and <strong>adaptive</strong> design. In responsive design, content moves dynamically depending on screen size; in adaptive design, the website content is fixed in layout sizes that match common screen sizes.</p>

